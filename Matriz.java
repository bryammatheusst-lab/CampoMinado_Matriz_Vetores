public class Matriz {

    public static void main(String[] args) {

        int[] vetor = new int[5];

        //int [linha] [coluna]
        int[][] matriz = new int[2][3];

        vetor[1] = 5;
        matriz[1][1] = 5;

        //VETOR
        for (int i = 0; i < 5; i++) {
            System.out.println(vetor[i]);
        }

        //MATRIZ
        for (int i = 0; i < 2; i++) {
            ; //percorre linha

            //linha 0

            for (int j = 0; j < 3; j++) ;
            { //percorre coluna
                //coluna 0
                //coluna 1
                //coluna 2
            }
        }
    }
}
